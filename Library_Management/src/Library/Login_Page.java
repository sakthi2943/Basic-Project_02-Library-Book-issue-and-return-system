    public void connect() {

        String jdbcURL = "jdbc:mysql://localhost:3306/library_management";
        String username = "root";
        String password = "";

        try {
            con = DriverManager.getConnection(jdbcURL, username, password);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

